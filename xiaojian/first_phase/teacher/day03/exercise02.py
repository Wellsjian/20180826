# 练习:在控制台中获取季度,显示该季度有多少月份.
#     春  -->  1  2  3
#     夏  -->  4  5  6
#     秋  -->  7  8  9
#     冬  -->  10 11 12

season = input("请输入季度:")
if season == "春":
  print("1  2  3")
if season == "夏":
  print("4  5  6")
if season == "秋":
  print("7  8  9")
if season == "冬":
  print("10 11 12")

